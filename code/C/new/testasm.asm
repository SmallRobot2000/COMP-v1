keyb_SET_ROW_:
    push ax
    push dx
    xchg al,ah
    mov dx,PPI_PORT_C
    in  al,dx
    and ah,0x0F
    and al,0xF0
    or  al,ah
    out dx,al
    pop dx
    pop ax
    ret

keyb_GET_COL_:

    push dx
    mov dx,PPI_PORT_B
    in  al,dx
    mov ah,0
    pop dx

    ret
chk_toggle_caps:


    test    al,0x08
    jz     chk_toggle_caps_no
    test    ah,keyFlags_caps_last
    jz      chk_toggle
    or     ah,keyFlags_caps_last
    ret
chk_toggle:
    
    xor     ah,keyFlags_caps ;new state - fliped last state
    or     ah,keyFlags_caps_last
    test    ah,keyFlags_caps
    jz      chk_caps_off
    call    keyb_CAPS_ON_
    ret
chk_caps_off:
    call    keyb_CAPS_OFF_
    ret
chk_toggle_caps_no:
    and     ah,keyFlags_caps ;last time is now and is off , all zero except caps bit  
    ret

