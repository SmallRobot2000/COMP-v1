print_hexbyte_:
        push    ax
        mov     ah, al  ; save a copy for lower nibble
        shr     al, 1   ; shift down upper nibble
        shr     al, 1
        shr     al, 1
        shr     al, 1
        add     al, '0' ; convert to ASCII
        cmp     al, '9' ; >= 10?
        jna     prA
	add     al, 'A'-'9'-1
prA:    int     06h
        mov     al, ah 
        and     al, 0Fh ; isolate lower nibble
        add     al, '0'
        cmp     al, '9'
        jna     prB
	add     al, 'A'-'9'-1
prB:    int     06h
        mov al,13
        int     06h
        mov al,10
        int     06h
        pop     ax
        ret




