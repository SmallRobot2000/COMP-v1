update_kyb_flags:
    push    ax
    push    bx
    push    es
    mov     ax,BIOS_VARIABLES_SEGMENT
    mov     es,ax
    mov     bx,BIOS_VARIABLES_OFFSET
    add     bx,BVAR_kyebFlags
